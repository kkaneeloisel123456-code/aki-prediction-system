# ============================================
# Chinese PDF Report Generator
# ============================================
class AKIReportPDF(FPDF):
    """Custom PDF class with Chinese support and AKI report template."""

    def __init__(self):
        super().__init__('P', 'mm', 'A4')
        # Add Unicode font for Chinese
        self._setup_fonts()
        self.set_auto_page_break(True, 20)

    def _setup_fonts(self):
        """Setup Chinese-capable fonts."""
        # Try common Chinese font paths on Windows
        font_paths = [
            'C:/Windows/Fonts/simhei.ttf',
            'C:/Windows/Fonts/msyh.ttf',
            'C:/Windows/Fonts/simsun.ttc',
            '/System/Library/Fonts/PingFang.ttc',
            '/usr/share/fonts/truetype/wqy/wqy-microhei.ttc',
        ]

        for path in font_paths:
            if os.path.exists(path):
                self.add_font('CJK', '', path, uni=True)
                self.add_font('CJK', 'B', path, uni=True)
                logger.info(f"Using font: {path}")
                return

        logger.warning("No CJK font found. Chinese characters may not render.")
        self.add_font('CJK', '', 'Helvetica')

    def header(self):
        """Page header."""
        self.set_font('CJK', 'B', 10)
        self.set_text_color(52, 152, 219)
        self.cell(0, 8, 'AKI Risk Assessment Report', align='C', new_x="LMARGIN", new_y="NEXT")
        self.line(self.get_x(), self.get_y(), self.get_x() + 190, self.get_y())
        self.ln(3)

    def footer(self):
        """Page footer."""
        self.set_y(-15)
        self.set_font('CJK', '', 8)
        self.set_text_color(128, 128, 128)
        self.cell(0, 8, f'Page {self.page_no()}/{{nb}} | Generated by AKI Intelligent Prediction System', align='C')

    def write_report(self, patient_info, prediction_result, risk_factors, recommendations, shap_fig=None):
        """
        Generate complete AKI risk assessment report.

        Args:
            patient_info: Dict with patient demographics
            prediction_result: Dict with probability, risk_level
            risk_factors: List of (feature, importance, direction)
            recommendations: Dict with clinical recommendations
            shap_fig: Matplotlib figure for SHAP explanation
        """
        self.alias_nb_pages()

        # ====== Cover Section ======
        self.add_page()
        self.ln(30)
        self.set_font('CJK', 'B', 28)
        self.set_text_color(44, 62, 80)
        self.cell(0, 15, 'AKI Risk Assessment Report', align='C', new_x="LMARGIN", new_y="NEXT")
        self.ln(5)
        self.set_font('CJK', '', 14)
        self.set_text_color(127, 140, 141)
        self.cell(0, 10, 'Acute Kidney Injury Intelligent Prediction System', align='C', new_x="LMARGIN", new_y="NEXT")
        self.ln(30)

        # Horizontal line
        self.set_draw_color(52, 152, 219)
        self.set_line_width(0.5)
        self.line(40, self.get_y(), 170, self.get_y())
        self.ln(10)

        # Patient ID and date
        self.set_font('CJK', '', 11)
        self.set_text_color(0, 0, 0)
        self.cell(0, 8, f"Report Date: {datetime.now().strftime('%Y-%m-%d %H:%M')}", align='C', new_x="LMARGIN", new_y="NEXT")
        self.cell(0, 8, f"Patient ID: {patient_info.get('id', 'N/A')}", align='C', new_x="LMARGIN", new_y="NEXT")
        self.ln(20)

        # Risk level badge
        risk_colors = {
            'Low': (39, 174, 96),     # Green
            'Medium': (243, 156, 18), # Orange
            'High': (231, 76, 60),    # Red
        }
        color = risk_colors.get(prediction_result.get('risk_level', 'Low'), (39, 174, 96))

        self.set_fill_color(*color)
        self.set_text_color(255, 255, 255)
        self.set_font('CJK', 'B', 20)
        risk_text = prediction_result.get('risk_level', 'Low').replace('Low', 'Low').replace('Medium', 'Medium').replace('High', 'High')
        self.cell(0, 15, f'Risk Level: {risk_text}', align='C', fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(5)
        self.set_font('CJK', 'B', 36)
        prob = prediction_result.get('probability', 0)
        self.cell(0, 20, f'{prob:.1%}', align='C', fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(15)

        # ====== Patient Information ======
        self.set_text_color(0, 0, 0)
        self.set_font('CJK', 'B', 14)
        self.set_fill_color(236, 240, 241)
        self.cell(0, 10, '1. Patient Information', fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(5)

        self.set_font('CJK', '', 10)
        info_items = [
            ('Age', patient_info.get('age', 'N/A')),
            ('Gender', patient_info.get('gender', 'N/A')),
            ('Surgery Type', patient_info.get('surgery_type', 'N/A')),
            ('APACHE II Score', patient_info.get('apache_ii', 'N/A')),
            ('Hypertension', patient_info.get('hypertension', 'N/A')),
            ('Diabetes', patient_info.get('diabetes', 'N/A')),
            ('Surgery Time', f"{patient_info.get('surgery_time', 'N/A')} min"),
        ]

        col_width = 95
        for i in range(0, len(info_items), 2):
            self.set_font('CJK', 'B', 10)
            self.cell(30, 8, info_items[i][0] + ':')
            self.set_font('CJK', '', 10)
            self.cell(65, 8, str(info_items[i][1]))
            if i + 1 < len(info_items):
                self.set_font('CJK', 'B', 10)
                self.cell(30, 8, info_items[i+1][0] + ':')
                self.set_font('CJK', '', 10)
                self.cell(65, 8, str(info_items[i+1][1]))
            self.ln(7)

        self.ln(5)

        # ====== Key Lab Results ======
        self.set_font('CJK', 'B', 14)
        self.set_fill_color(236, 240, 241)
        self.cell(0, 10, '2. Key Laboratory Results', fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(5)

        lab_keys = ['preop_scr', 'preop_egfr', 'preop_alb', 'preop_hb', 'preop_wbc',
                     'preop_crp', 'preop_lactate', 'preop_nlr', 'preop_bnp']
        lab_labels = ['Serum Creatinine (Scr)', 'eGFR', 'Albumin', 'Hemoglobin', 'WBC',
                      'CRP', 'Lactate', 'NLR', 'BNP']
        lab_units = ['umol/L', 'ml/min/1.73m2', 'g/L', 'g/L', 'x10^9/L',
                      'mg/L', 'mmol/L', '', 'pg/mL']

        # Table header
        self.set_font('CJK', 'B', 9)
        self.set_fill_color(52, 152, 219)
        self.set_text_color(255, 255, 255)
        self.cell(70, 7, 'Test', border=1, fill=True)
        self.cell(40, 7, 'Value', border=1, fill=True, align='C')
        self.cell(40, 7, 'Unit', border=1, fill=True, align='C')
        self.cell(40, 7, 'Reference Range', border=1, fill=True, align='C')
        self.ln()

        self.set_text_color(0, 0, 0)
        for key, label, unit in zip(lab_keys, lab_labels, lab_units):
            value = patient_info.get(key, 'N/A')
            self.set_font('CJK', '', 9)
            self.cell(70, 7, f'  {label}', border=1)
            self.cell(40, 7, str(value), border=1, align='C')
            self.cell(40, 7, unit, border=1, align='C')
            self.cell(40, 7, '', border=1, align='C')
            self.ln()

        self.ln(8)

        # ====== Risk Factors ======
        self.set_font('CJK', 'B', 14)
        self.set_fill_color(236, 240, 241)
        self.cell(0, 10, '3. Top Risk Factors', fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(5)

        self.set_font('CJK', 'B', 9)
        self.set_fill_color(231, 76, 60)
        self.set_text_color(255, 255, 255)
        self.cell(15, 7, 'Rank', border=1, fill=True, align='C')
        self.cell(60, 7, 'Risk Factor', border=1, fill=True)
        self.cell(40, 7, 'Impact', border=1, fill=True, align='C')
        self.cell(75, 7, 'Clinical Interpretation', border=1, fill=True)
        self.ln()

        self.set_text_color(0, 0, 0)
        for rank, factor in enumerate(risk_factors[:5], 1):
            self.set_font('CJK', '', 9)
            self.cell(15, 12, str(rank), border=1, align='C')
            self.cell(60, 12, f' {factor[0]}', border=1)
            direction = 'Risk (+)' if factor[2] == 'positive' else 'Protective (-)'
            self.cell(40, 12, direction, border=1, align='C')

            # Clinical interpretation
            from web.components.shap_explain import get_clinical_explanation
            explanation = get_clinical_explanation(factor[0], factor[1], factor[2])
            self.set_font('CJK', '', 7)
            self.cell(75, 12, explanation[:80], border=1)
            self.ln()

        self.ln(8)

        # ====== SHAP Explanation ======
        if shap_fig is not None:
            self.set_font('CJK', 'B', 14)
            self.set_fill_color(236, 240, 241)
            self.cell(0, 10, '4. AI Explanation (SHAP)', fill=True, new_x="LMARGIN", new_y="NEXT")
            self.ln(5)

            # Save SHAP figure temporarily and insert
            tmp_path = Path(__file__).parent.parent.parent / 'outputs' / 'tmp_shap.png'
            shap_fig.savefig(tmp_path, dpi=150, bbox_inches='tight')
            self.image(str(tmp_path), x=20, w=170)
            self.ln(5)

        # ====== Clinical Recommendations ======
        self.add_page()
        self.set_font('CJK', 'B', 14)
        self.set_fill_color(236, 240, 241)
        self.cell(0, 10, '5. Clinical Recommendations', fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(8)

        sections = ['monitoring', 'prevention']
        section_titles = ['Monitoring Recommendations', 'Prevention Strategies']

        for section, title in zip(sections, section_titles):
            self.set_font('CJK', 'B', 12)
            self.set_text_color(41, 128, 185)
            self.cell(0, 8, title, new_x="LMARGIN", new_y="NEXT")
            self.ln(2)

            items = recommendations.get(section, [])
            for item in items:
                self.set_font('CJK', '', 10)
                self.set_text_color(0, 0, 0)
                self.cell(5, 6, '')
                self.cell(5, 6, chr(8226))
                self.cell(0, 6, item, new_x="LMARGIN", new_y="NEXT")
            self.ln(5)

        # Follow-up
        self.set_font('CJK', 'B', 12)
        self.set_text_color(41, 128, 185)
        self.cell(0, 8, 'Follow-up', new_x="LMARGIN", new_y="NEXT")
        self.ln(2)
        self.set_font('CJK', '', 10)
        self.set_text_color(0, 0, 0)
        self.multi_cell(0, 6, recommendations.get('follow_up', ''))

        self.ln(8)

        # KDIGO Reference
        self.set_font('CJK', 'B', 14)
        self.set_fill_color(236, 240, 241)
        self.cell(0, 10, '6. KDIGO AKI Diagnostic Criteria (Reference)', fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(5)
        self.set_font('CJK', '', 9)
        self.multi_cell(0, 5,
            "AKI is diagnosed when any ONE of the following criteria is met:\n"
            "- Increase in SCr by >= 0.3 mg/dL (>= 26.5 umol/L) within 48 hours; OR\n"
            "- Increase in SCr to >= 1.5 times baseline within the prior 7 days; OR\n"
            "- Urine volume < 0.5 mL/kg/h for 6 hours.\n\n"
            "KDIGO Staging:\n"
            "- Stage 1: SCr 1.5-1.9x baseline OR >= 0.3 mg/dL increase OR UO < 0.5 mL/kg/h for 6-12h\n"
            "- Stage 2: SCr 2.0-2.9x baseline OR UO < 0.5 mL/kg/h for >= 12h\n"
            "- Stage 3: SCr 3.0x baseline OR >= 4.0 mg/dL OR RRT initiation OR UO < 0.3 mL/kg/h for >= 24h"
        )

        # ====== Disclaimer ======
        self.ln(10)
        self.set_font('CJK', '', 8)
        self.set_text_color(149, 165, 166)
        self.multi_cell(0, 4,
            'Disclaimer: This report is generated by an AI-powered prediction system for '
            'academic research and clinical reference purposes only. It should NOT be used '
            'as the sole basis for clinical decision-making. All clinical decisions should be '
            'made by qualified healthcare professionals based on comprehensive patient evaluation.'
        )


def generate_pdf_report(patient_info, prediction_result, risk_factors,
                         recommendations, shap_fig=None, output_path=None):
    """
    Generate and save PDF report.

    Args:
        patient_info: Dict with patient demographics and lab values
        prediction_result: Dict with 'probability' and 'risk_level'
        risk_factors: List of (feature, importance, direction) tuples
        recommendations: Dict with clinical recommendations
        shap_fig: Optional matplotlib figure for SHAP explanation
        output_path: Output file path

    Returns:
        output_path: Path to generated PDF
    """
    if output_path is None:
        output_dir = Path(__file__).parent.parent.parent / 'outputs' / 'reports'
        output_dir.mkdir(parents=True, exist_ok=True)
        output_path = output_dir / f'AKI_Report_{datetime.now().strftime("%Y%m%d_%H%M%S")}.pdf'

    pdf = AKIReportPDF()
    pdf.write_report(patient_info, prediction_result, risk_factors, recommendations, shap_fig)

    pdf.output(str(output_path))
    logger.info(f"PDF report generated: {output_path}")

    return output_path


