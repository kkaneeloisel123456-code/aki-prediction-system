        self.cell(0, 7, f"Patient ID: {patient_info.get('id', 'N/A')}", align='C', new_x="LMARGIN", new_y="NEXT")

        # Stars + KDIGO
        if risk_report:
            self.ln(3)
            self.set_font('CJK', 'B', 11)
            stars = risk_report.get('stars_display', '')
            self.cell(0, 8, f'Risk: {stars}  |  {risk_report.get("kdigo_stage", "")}', align='C', new_x="LMARGIN", new_y="NEXT")

